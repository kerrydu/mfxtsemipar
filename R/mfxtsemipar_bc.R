# ==============================================================================
# Helper: compute uniform confidence band for the bias-corrected curve
# ==============================================================================
compute_ucb_cv3 <- function(object,
                            newdata,
                            uvar = NULL,
                            level = 0.95,
                            sim_reps = 2000L) {
  if (!inherits(object, "mfxtsemipar_cv3")) {
    stop("object must be of class 'mfxtsemipar_cv3'.")
  }

  if (is.numeric(newdata)) {
    x <- newdata
    if (is.null(uvar)) uvar <- object$uvar
  } else {
    newdata <- data.table::as.data.table(newdata)
    if (is.null(uvar)) uvar <- object$uvar
    if (!(uvar %in% names(newdata))) {
      stop("Variable '", uvar, "' not found in newdata.")
    }
    x <- newdata[[uvar]]
  }

  if (!is.numeric(x)) {
    stop("Evaluation variable must be numeric.")
  }

  if (is.null(object$coef_bc)) {
    stop("UCB requires bias_correct = TRUE.")
  }

  bc_type <- object$bc_type

  # Main spline basis on the grid
  sp <- make_splines(
    x = x,
    type = object$type,
    knots = object$knots,
    bknots = object$bknots,
    degree = object$degree,
    center = object$center,
    intercept = object$intercept,
    prefix = ".Spline_"
  )
  B <- sp$matrix
  spline_vars <- sp$names

  retained <- intersect(names(object$coef), spline_vars)
  if (length(retained) == 0L) {
    stop("No spline coefficients available for UCB.")
  }
  B_retained <- B[, retained, drop = FALSE]

  retained_bc <- object$bc_retained_bc

  # BC basis on the grid
  sp_bc <- make_splines(
    x = x,
    type = object$type,
    knots = if (is.null(object$bc_knots)) object$knots else object$bc_knots,
    bknots = object$bknots,
    degree = object$bc_degree,
    center = object$center,
    intercept = object$intercept,
    prefix = ".SplineBC_"
  )
  Btilde <- sp_bc$matrix
  Btilde_retained <- Btilde[, retained_bc, drop = FALSE]

  all_retained <- c(retained, retained_bc)
  b_joint <- object$coef_bc[all_retained]

  # Prefer wild-bootstrap covariance if available
  vcov_use <- if (bc_type == "bc1" && !is.null(object$vcov_bc1_boot)) {
    object$vcov_bc1_boot
  } else if (!is.null(object$vcov_bc_boot)) {
    object$vcov_bc_boot
  } else {
    object$vcov_bc[all_retained, all_retained, drop = FALSE]
  }
  vcov_use <- vcov_use[all_retained, all_retained, drop = FALSE]

  if (bc_type == "bc1") {
    w <- cbind(
      matrix(0, nrow = nrow(B_retained), ncol = length(retained)),
      Btilde_retained
    )
  } else {
    proj_main <- object$bc_G0 %*% object$bc_M[, retained_bc, drop = FALSE]
    Btilde_orth_retained <- Btilde_retained - B_retained %*% proj_main
    w <- cbind(B_retained, Btilde_orth_retained)
  }

  y <- as.numeric(w %*% b_joint)
  se <- sqrt(pmax(0, rowSums((w %*% vcov_use) * w)))

  draws <- simulate_mvnorm(sim_reps, vcov_use)
  deviations <- draws %*% t(w)

  se_safe <- pmax(se, .Machine$double.eps)
  t_stats <- apply(abs(deviations) / matrix(se_safe, nrow = sim_reps,
                                            ncol = length(se_safe), byrow = TRUE),
                   1, max)
  crit <- as.numeric(stats::quantile(t_stats, probs = level, na.rm = TRUE))

  list(
    grid = data.table::data.table(
      u = x,
      g = y,
      se = se,
      lb = y - crit * se,
      ub = y + crit * se
    ),
    crit = crit,
    level = level,
    sim_reps = sim_reps
  )
}
