#' @export
print.mfxtsemipar_tensor <- function(x, ...) {
  cat("Mixed-frequency tensor-product semiparametric regression\n")
  cat("  High-frequency variable: ", x$uvar_hf, "\n", sep = "")
  cat("  Low-frequency moderator(s): ", paste(x$uvar_lf, collapse = ", "), "\n", sep = "")
  cat("  Selected knots: ", paste(x$nknots, collapse = " x "), "\n", sep = "")
  cat("  Minimum CV RMSE: ", round(x$min_cv_mse, 6), "\n", sep = "")
  cat("  Final fit RMSE: ", round(x$rmse, 6), "\n", sep = "")
  cat("  Simple optimal knots: ", paste(x$soptnk, collapse = " x "), "\n", sep = "")
  bc_type <- if (!is.null(x$bc_type)) paste0(x$bc_type, " (degree ",
                                              paste(x$bc_degree, collapse = "/"), ")") else "none"
  cat("  Bias correction: ", bc_type, "\n", sep = "")
  cat("\nCV RMSE by knot combination:\n")
  print(x$cv_mse, row.names = FALSE)
  invisible(x)
}


