  out <- data.table::as.data.table(B_tensor)
  out[, (keys) := B_lf[, keys, with = FALSE]]
  data.table::setcolorder(out, c(keys, vars))
  out
}


# ==============================================================================
# Helper: build high-frequency tensor-product basis from knots
# B_hf = B_hf(z1) * B_lf(z2 replicated) * B_lf(z3 replicated)
# ==============================================================================
build_hf_tensor_basis_from_knots <- function(hf, uvar_hf, uvar_lf, knots, type,
                                             bknots, degree, center,
                                             prefix = ".Spline_") {
  d <- 1L + length(uvar_lf)

  sp1 <- make_splines(
    x = hf[[uvar_hf]],
    type = type[1L],
    knots = knots[[1L]],
    bknots = bknots[[1L]],
    degree = degree[1L],
    center = center[[1L]],
    intercept = TRUE,
    prefix = paste0(prefix, "1_")
  )

  bases <- list(sp1$matrix)

  for (j in seq_along(uvar_lf)) {
    jj <- j + 1L
    sp_j <- make_splines(
      x = hf[[uvar_lf[j]]],
      type = type[jj],
      knots = knots[[jj]],
      bknots = bknots[[jj]],
      degree = degree[jj],
      center = center[[jj]],
      intercept = TRUE,
      prefix = paste0(prefix, jj, "_")
    )
    bases[[jj]] <- sp_j$matrix
  }

  B_tensor <- bases[[1L]]
  for (j in seq(2L, d)) {
    B_tensor <- khatri_rao(B_tensor, bases[[j]])
  }

  dim_idx <- expand.grid(lapply(seq_len(d), function(j) seq_len(ncol(bases[[j]]))))
  vars <- apply(dim_idx, 1L, function(idx) paste0(prefix, paste(idx, collapse = "_")))
  colnames(B_tensor) <- vars

  B_tensor
}


# ==============================================================================
# Helper: row-wise Khatri-Rao product
# ==============================================================================
khatri_rao <- function(A, B) {
  n <- nrow(A)
  if (nrow(B) != n) stop("Matrices must have the same number of rows.")
  p <- ncol(A)
  q <- ncol(B)
  out <- matrix(0, nrow = n, ncol = p * q)
  for (i in seq_len(n)) {
    out[i, ] <- as.numeric(outer(A[i, ], B[i, ]))
  }
  out
}


# ==============================================================================
# Helper: parse bc_nknots for tensor product
# ==============================================================================
parse_bc_nknots_tensor <- function(bc_nknots, nknots) {
  d <- length(nknots)
  if (is.null(bc_nknots)) return(rep(NA_integer_, d))

  if (is.character(bc_nknots) && length(bc_nknots) == 1L &&
      grepl("k$", bc_nknots, ignore.case = TRUE)) {
    mult <- suppressWarnings(as.numeric(sub("k$", "", bc_nknots, ignore.case = TRUE)))
    if (!is.finite(mult) || mult <= 0) stop("bc_nknots multiplier must be positive.")
    return(pmax(1L, as.integer(round(nknots * mult))))
  }

  if (is.numeric(bc_nknots)) {
    if (length(bc_nknots) == 1L) {
      bc_nknots <- rep(as.integer(round(bc_nknots)), d)
    }
    if (length(bc_nknots) != d) {
      stop("bc_nknots must have length 1 or length 1 + length(uvar_lf).")
    }
    return(as.integer(round(bc_nknots)))
  }

  stop("bc_nknots must be NULL, a positive integer (vector), or a string like '1.5k'.")
}


# ==============================================================================
# Helper: compute uniform confidence band
# ==============================================================================
compute_ucb_tensor <- function(object,
                               newdata,
                               uvar_hf,
                               uvar_lf,
                               level = 0.95,
                               sim_reps = 2000L) {
  if (!inherits(object, "mfxtsemipar_tensor")) {
    stop("object must be of class 'mfxtsemipar_tensor'.")
  }
  if (is.null(object$coef_bc)) {
    stop("UCB requires bias_correct = TRUE.")
  }

  all_uvars <- c(uvar_hf, uvar_lf)
  d <- length(all_uvars)

  if (is.null(newdata)) {
    grid_list <- lapply(seq_len(d), function(j) {
      uj <- if (j == 1L) object$fitted[[uvar_hf]] else object$fitted[[uvar_lf[j - 1L]]]
      seq(min(uj, na.rm = TRUE), max(uj, na.rm = TRUE), length.out = 50L)
    })
    newdata <- do.call(expand.grid, c(grid_list, stringsAsFactors = FALSE))
    names(newdata) <- all_uvars
  }
  newdata <- data.table::as.data.table(newdata)

  B <- build_hf_tensor_basis_from_knots(
    hf = newdata,
    uvar_hf = uvar_hf,
    uvar_lf = uvar_lf,
    knots = object$knots,
    type = object$type,
    bknots = object$bknots,
    degree = object$degree,
    center = as.list(object$center),
    prefix = ".Spline_"
  )
  spline_vars <- colnames(B)

  retained <- intersect(names(object$coef), spline_vars)
  if (length(retained) == 0L) stop("No spline coefficients available for UCB.")
  B_retained <- B[, retained, drop = FALSE]

  retained_bc <- object$bc_retained_bc

  Bc <- build_hf_tensor_basis_from_knots(
    hf = newdata,
    uvar_hf = uvar_hf,
    uvar_lf = uvar_lf,
    knots = object$bc_knots,
    type = object$type,
    bknots = object$bknots,
    degree = object$bc_degree,
    center = as.list(object$center),
    prefix = ".SplineBC_"
  )
  Bc_retained <- Bc[, retained_bc, drop = FALSE]

  all_retained <- c(retained, retained_bc)
  b_joint <- object$coef_bc[all_retained]
  V_joint <- object$vcov_bc[all_retained, all_retained, drop = FALSE]

  bc_type <- object$bc_type
  if (bc_type == "bc1") {
    w <- cbind(matrix(0, nrow = nrow(B_retained), ncol = length(retained)),
               Bc_retained)
  } else {
    proj_main <- object$bc_G0 %*% object$bc_M[, retained_bc, drop = FALSE]
    Bc_orth_retained <- Bc_retained - B_retained %*% proj_main
    w <- cbind(B_retained, Bc_orth_retained)
  }

  y <- as.numeric(w %*% b_joint)
  se <- sqrt(pmax(0, rowSums((w %*% V_joint) * w)))

  draws <- simulate_mvnorm(sim_reps, V_joint)
  deviations <- draws %*% t(w)

  se_safe <- pmax(se, .Machine$double.eps)
  t_stats <- apply(abs(deviations) / matrix(se_safe, nrow = sim_reps,
                                            ncol = length(se_safe), byrow = TRUE),
                   1, max)
  crit <- as.numeric(stats::quantile(t_stats, probs = level, na.rm = TRUE))

  out <- data.table::copy(newdata)
  out[, g := y]
  out[, se := se]
  out[, lb := g - crit * se]
  out[, ub := g + crit * se]

  list(grid = out, crit = crit, level = level, sim_reps = sim_reps)
}


